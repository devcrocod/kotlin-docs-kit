// Concatenate src/components/*.css into dist/components.css in a fixed,
// layered order (foundations → content → navigation → reference → layout →
// states). Each section is preceded by a header comment so consumers can
// locate component CSS in the bundled file.

import { readFile, readdir, writeFile, mkdir } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, '..');

const ORDER = [
  // Foundations
  'buttons.css',
  'pill.css',
  'toggle.css',
  'segmented.css',
  'inputs.css',
  'badges.css',
  'tags.css',
  'methods.css',
  // Content
  'callouts.css',
  'codeblock.css',
  'cards.css',
  'tabs.css',
  'accordion.css',
  'hero.css',
  // Navigation
  'topnav.css',
  'nav.css',
  'toc.css',
  'search-ui.css',
  'pager.css',
  'related.css',
  // Reference
  'params.css',
  'targets.css',
  // Layout
  'eyebrow.css',
  'article.css',
  'shell.css',
  'copy-page.css',
  // States
  'states.css',
];

const componentsDir = resolve(pkgRoot, 'src/components');
const present = (await readdir(componentsDir)).filter((name) => name.endsWith('.css')).sort();
const expected = [...ORDER].sort();

if (present.length !== expected.length || present.some((n, i) => n !== expected[i])) {
  const onlyInDir = present.filter((n) => !ORDER.includes(n));
  const onlyInOrder = ORDER.filter((n) => !present.includes(n));
  throw new Error(
    `Component list out of sync.\n` +
      (onlyInDir.length ? `  in src/components/ but not in ORDER: ${onlyInDir.join(', ')}\n` : '') +
      (onlyInOrder.length ? `  in ORDER but not on disk: ${onlyInOrder.join(', ')}\n` : '') +
      'Update ORDER in scripts/build-components.mjs.',
  );
}

const header = (name) => `/* ===== ${name} ${'='.repeat(Math.max(0, 70 - name.length))} */\n`;

const parts = [
  '/* @ktdocs/tokens — components.css. Generated by scripts/build-components.mjs.\n' +
    '   Concatenates src/components/*.css in layered order. Do not edit directly. */\n\n',
];

for (const file of ORDER) {
  const css = await readFile(resolve(componentsDir, file), 'utf8');
  parts.push(header(file));
  parts.push(css.trim() + '\n\n');
}

await mkdir(resolve(pkgRoot, 'dist'), { recursive: true });
await writeFile(resolve(pkgRoot, 'dist/components.css'), parts.join(''));

console.log(`Built dist/components.css from ${ORDER.length} sources.`);
