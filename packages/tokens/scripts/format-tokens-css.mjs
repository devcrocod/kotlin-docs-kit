import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { cssNameFromPath } from './name-utils.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, '..');

const reducedMotion = readFileSync(resolve(pkgRoot, 'src/_static/reduced-motion.css'), 'utf8');

const endsWith = (token, suffix) => token.filePath.replace(/\\/g, '/').endsWith(suffix);

const FILTERS = {
  spacing: (t) => endsWith(t, 'src/spacing.json'),
  radius: (t) => endsWith(t, 'src/radius.json'),
  shadow: (t) => endsWith(t, 'src/shadow.json'),
  motion: (t) => endsWith(t, 'src/motion.json'),
  zIndex: (t) => endsWith(t, 'src/z-index.json'),
  layout: (t) => endsWith(t, 'src/layout.json'),
};

const declLine = (token) => `  --${token.name}: ${token.$value};`;
const indented = (lines, prefix = '  ') => lines.map((l) => prefix + l);
const block = (selector, lines) => `${selector} {\n${lines.join('\n')}\n}\n`;

// shadow.dark is dropped from SD's source by the preprocess parser to avoid
// name collisions. The formatter reads it directly here and emits the
// `[data-theme="dark"]` block (and the auto-dark @media block) from the raw
// JSON. Only `$value` strings are present in this subtree; no references.
function shadowDarkDecls() {
  const data = JSON.parse(readFileSync(resolve(pkgRoot, 'src/shadow.json'), 'utf8'));
  const dark = data?.shadow?.dark ?? {};
  return Object.entries(dark)
    .filter(([key]) => !key.startsWith('$'))
    .map(([key, leaf]) => `  --${cssNameFromPath(['shadow', key])}: ${leaf.$value};`);
}

export function kitTokensCssFormat({ dictionary }) {
  const all = dictionary.allTokens;

  const lightRootLines = [
    '  /* SPACING — 4px base grid */',
    ...all.filter(FILTERS.spacing).map(declLine),
    '',
    '  /* RADIUS */',
    ...all.filter(FILTERS.radius).map(declLine),
    '',
    '  /* SHADOW */',
    ...all.filter(FILTERS.shadow).map(declLine),
    '',
    '  /* MOTION */',
    ...all.filter(FILTERS.motion).map(declLine),
    '',
    '  /* Z-INDEX */',
    ...all.filter(FILTERS.zIndex).map(declLine),
    '',
    '  /* LAYOUT */',
    ...all.filter(FILTERS.layout).map(declLine),
  ];

  const darkShadowLines = shadowDarkDecls();

  const banner =
    `/* ============================================================================\n` +
    `   Kotlin Docs Theme System — Spacing, Radius, Shadow, Motion, Z-index, Layout\n` +
    `   Generated by Style Dictionary from packages/tokens/src/. Do not edit.\n` +
    `   ============================================================================ */\n\n`;

  const autoDarkBlock =
    `@media (prefers-color-scheme: dark) {\n` +
    `  :root:not([data-theme="light"]) {\n` +
    `${indented(darkShadowLines, '  ').join('\n')}\n` +
    `  }\n` +
    `}\n`;

  return (
    banner +
    block(':root', lightRootLines) +
    '\n/* Dark-theme shadow overrides */\n' +
    block('[data-theme="dark"]', darkShadowLines) +
    '\n' +
    autoDarkBlock +
    '\n' +
    reducedMotion
  );
}
