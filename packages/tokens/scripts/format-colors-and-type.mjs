import { readFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { cssNameFromPath } from './name-utils.mjs';

const here = dirname(fileURLToPath(import.meta.url));
const pkgRoot = resolve(here, '..');

const typeUtilities = readFileSync(resolve(pkgRoot, 'src/_static/type-utilities.css'), 'utf8');
const elementDefaults = readFileSync(resolve(pkgRoot, 'src/_static/element-defaults.css'), 'utf8');

const endsWith = (token, suffix) => token.filePath.replace(/\\/g, '/').endsWith(suffix);

const FILTERS = {
  primitive: (t) => endsWith(t, 'src/color/primitive.json'),
  neutral: (t) => endsWith(t, 'src/color/neutral.json'),
  semanticPrim: (t) => endsWith(t, 'src/color/semantic-primitive.json'),
  gradient: (t) => endsWith(t, 'src/color/gradient.json'),
  semanticLight: (t) => endsWith(t, 'src/color/semantic.light.json'),
  families: (t) => endsWith(t, 'src/typography/families.json'),
  scale: (t) => endsWith(t, 'src/typography/scale.json'),
};

const REF_RE = /^\{([A-Za-z0-9_.-]+)\}$/;

const declLine = (token) => `  --${token.name}: ${token.$value};`;
const indented = (lines, prefix = '  ') => lines.map((l) => prefix + l);
const block = (selector, lines) => `${selector} {\n${lines.join('\n')}\n}\n`;

const section = (title) =>
  `\n/* ----------------------------------------------------------------------------\n` +
  `   ${title}\n` +
  `   ---------------------------------------------------------------------------- */\n`;

// Render a DTCG $value for emission. Handles reference strings, arrays
// (fontFamily / cubicBezier — currently unused in semantic.dark but covered for
// safety), and pass-through for primitives.
function renderValue($type, $value) {
  if (typeof $value === 'string') {
    const ref = $value.match(REF_RE);
    if (ref) return `var(--${cssNameFromPath(ref[1].split('.'))})`;
    return $value;
  }
  if (Array.isArray($value)) {
    if ($type === 'fontFamily') {
      return $value.map((f) => (/\s/.test(f) ? `"${f}"` : f)).join(', ');
    }
    if ($type === 'cubicBezier') {
      return `cubic-bezier(${$value.join(', ')})`;
    }
    return $value.join(', ');
  }
  return String($value);
}

// Walk a parsed DTCG JSON tree and emit a CSS declaration for each leaf.
function emitDeclsFromJson(filePath) {
  const data = JSON.parse(readFileSync(filePath, 'utf8'));
  const out = [];
  const walk = (node, path) => {
    if (node === null || typeof node !== 'object' || Array.isArray(node)) return;
    if ('$value' in node) {
      const name = cssNameFromPath(path);
      out.push(`  --${name}: ${renderValue(node.$type, node.$value)};`);
      return;
    }
    for (const [key, child] of Object.entries(node)) {
      if (key.startsWith('$')) continue;
      walk(child, [...path, key]);
    }
  };
  walk(data, []);
  return out;
}

export function kitColorsAndTypeFormat({ dictionary }) {
  const all = dictionary.allTokens;

  const primitives = [
    ...all.filter(FILTERS.primitive),
    ...all.filter(FILTERS.neutral),
    ...all.filter(FILTERS.semanticPrim),
    ...all.filter(FILTERS.gradient),
  ].map(declLine);

  const lightDecls = all.filter(FILTERS.semanticLight).map(declLine);

  // semantic.dark is skipped by SD (see style-dictionary.config.mjs parsers)
  // because it shares paths with semantic.light; we walk the JSON directly here.
  const darkDecls = emitDeclsFromJson(resolve(pkgRoot, 'src/color/semantic.dark.json'));

  const families = all.filter(FILTERS.families).map(declLine);
  const scale = all.filter(FILTERS.scale).map(declLine);

  const banner =
    `/* ============================================================================\n` +
    `   Kotlin Docs Theme System — Colors & Typography Tokens\n` +
    `   Generated by Style Dictionary from packages/tokens/src/. Do not edit.\n` +
    `   ============================================================================ */\n` +
    `\n@import "./fonts.css";\n`;

  const autoDarkInner = indented(darkDecls, '  ');
  const autoDarkBlock =
    `@media (prefers-color-scheme: dark) {\n` +
    `  :root:not([data-theme="light"]) {\n` +
    `    color-scheme: dark;\n` +
    `${autoDarkInner.join('\n')}\n` +
    `  }\n` +
    `}\n`;

  return (
    banner +
    section('1. PRIMITIVE COLOR TOKENS — Kotlin palette') +
    block(':root', primitives) +
    section('2. SEMANTIC TOKENS — LIGHT THEME (default)') +
    block(':root,\n[data-theme="light"]', ['  color-scheme: light;', ...lightDecls]) +
    section('3. SEMANTIC TOKENS — DARK THEME') +
    block('[data-theme="dark"]', ['  color-scheme: dark;', ...darkDecls]) +
    section('3b. AUTO DARK via prefers-color-scheme') +
    autoDarkBlock +
    section('4. TYPOGRAPHY') +
    block(':root', [...families, ...scale]) +
    '\n' +
    typeUtilities +
    section('5. ELEMENT DEFAULTS') +
    elementDefaults
  );
}
